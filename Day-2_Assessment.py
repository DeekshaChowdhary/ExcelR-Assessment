# Create and access List, Tuple, and Dictionary

my_list = [10, 20, 30, 40, 50]
print("List:", my_list)
print("Access first element:", my_list[0])
print("Access last element:", my_list[-1])

my_tuple = ("apple", "banana", "cherry", "date", "elderberry")
print("\nTuple:", my_tuple)
print("Access second element:", my_tuple[1])
print("Access fourth element:", my_tuple[3])

my_dict = {"a": 1, "b": 2, "c": 3, "d": 4, "e": 5}
print("\nDictionary:", my_dict)
print("Access value for key 'a':", my_dict["a"])
print("Access value for key 'e':", my_dict["e"])
